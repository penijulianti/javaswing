    private void simpanKeranjangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanKeranjangActionPerformed
        // TODO add your handling code here:
        String kode = (String) namaBarang.getSelectedItem(); 
        String kodeFix = kode.substring(0, 4);
        String namaBrg = kode.substring(5, kode.length());
        double hrg = Double.parseDouble(harga.getText());
        int jml = Integer.parseInt(jumlah.getText());
        double subTot = hrg * jml;

        Object[] data = new Object[5];
        data[0] = kodeFix;
        data[1] = namaBrg;
        data[2] = String.valueOf(hrg);
        data[3] = String.valueOf(jml);
        data[4] = String.valueOf(subTot);

        model.addRow(data);
        this.hitungTotalBayar();
        this.kosongkanData();
    }//GEN-LAST:event_simpanKeranjangActionPerformed

