    private void hitungTotalBayar () {
        int jmlRow = tabelTransaksi.getRowCount();
    double tot = 0;
    if (jmlRow == 0) {
        return;
    } else {
        for (int i = 0; i < jmlRow; i++) {
            double jmlTot = Double.valueOf(String.valueOf(model.getValueAt(i, 4)));
            tot += jmlTot;
        }
    }
    totalBayar.setText(String.valueOf(tot));
    }
    
    



