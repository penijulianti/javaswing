    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        try {
            koneksi();
            int row = jTable1.getSelectedRow();
            String tabel_klik = (jTable1.getModel().getValueAt(row, 0).toString());
            java.sql.Statement stm = con.createStatement();
            java.sql.ResultSet sql = stm.executeQuery("select * From user where id_user='" + tabel_klik + "'");
            if (sql.next()) {
                String ID = sql.getString("id_user");
                jTextField1.setText(ID);
                String Nama = sql.getString("nama");
                jTextField2.setText(Nama);
                String Alamat = sql.getString("alamat");
                jTextField3.setText(Alamat);
                String telepon = sql.getString("telepon");
                jTextField4.setText(telepon);
                String username = sql.getString("username");
                txtuserlogin.setText(username);
                String password = sql.getString("password");
                txtpas.setText(password);
                String akses = sql.getString("akses");
                jTextField7.setText(akses);
                String status = sql.getString("status");
            switch (status) {
                case "Aktif":
                    jComboBox1.setSelectedIndex(1); // Indeks 1 untuk "Aktif"
                    break;
                case "Tidak Aktif":
                    jComboBox1.setSelectedIndex(2); // Indeks 2 untuk "Tidak Aktif"
                    break;
                default:
                    jComboBox1.setSelectedIndex(0); // Indeks 0 untuk opsi "..Pilih.."
                    break;
            }

            }

        } catch (Exception e) {
        }
    }//GEN-LAST:event_jTable1MouseClicked

