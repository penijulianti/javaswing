    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
                                      
    try {
        koneksi();
        
        // Mendapatkan indeks baris yang dipilih
        int row = jTable1.getSelectedRow();
        
        // Mengambil nilai id_user dari kolom pertama (indeks 0)
        String idUser = jTable1.getModel().getValueAt(row, 0).toString();
        
        // Query SQL untuk update data berdasarkan id_user
        String SQL = "UPDATE user SET id_user = ?, nama = ?, alamat = ?, telepon = ?, username = ?, password = ?, akses = ?, status = ? WHERE id_user = ?";
        pst = con.prepareStatement(SQL);
        
        // Mengisi parameter query dengan nilai dari field-text dan jComboBox
        pst.setString(1, jTextField1.getText());
        pst.setString(2, jTextField2.getText());
        pst.setString(3, jTextField3.getText());
        pst.setString(4, jTextField4.getText());
        pst.setString(5, txtuserlogin.getText());
        pst.setString(6, txtpas.getText());
        pst.setString(7, jTextField7.getText());
        pst.setString(8, jComboBox1.getSelectedItem().toString());
        pst.setString(9, idUser); // id_user untuk kondisi WHERE
        
        // Eksekusi query
        int rowsAffected = pst.executeUpdate();

        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(null, "Data Berhasil Diubah");
            
            // Memperbarui tabel setelah ubah
            display();
            
            // Membersihkan field-text setelah ubah
            Clear();
        } else {
            JOptionPane.showMessageDialog(null, "Tidak ada baris yang terpengaruh atau id_user tidak ditemukan.");
        }
    } catch (Exception exc) {
        JOptionPane.showMessageDialog(null, "Error: " + exc.getMessage());
    } finally {
        try {
            if (pst != null) {
                pst.close();
            }
            if (con != null) {
                con.close();
            }
        } catch (Exception e) {
            // Tangani penutupan statement dan koneksi
        }
    }
    }//GEN-LAST:event_jButton3ActionPerformed

