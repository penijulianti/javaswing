    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            koneksi();

            // Mendapatkan nilai dari jComboBox
            String selectedStatus = jComboBox1.getSelectedItem().toString();

            // Query SQL untuk insert data
            String SQL = "insert into user (id_user, nama, alamat, telepon, username, password, akses, status) values (?, ?, ?, ?, ?, ?, ?, ?)";
            pst = con.prepareStatement(SQL);

            // Mengisi parameter query dengan nilai dari field-text
            pst.setString(1, jTextField1.getText());
            pst.setString(2, jTextField2.getText());
            pst.setString(3, jTextField3.getText());
            pst.setString(4, jTextField4.getText());
            pst.setString(5, txtuserlogin.getText());
            pst.setString(6, txtpas.getText());
            pst.setString(7, jTextField7.getText());
            pst.setString(8, selectedStatus); // Menggunakan nilai dari jComboBox

            // Eksekusi query
            pst.executeUpdate();

            display(); // Memperbarui tabel setelah simpan

            Clear(); // Membersihkan field-text setelah simpan

            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
        } catch (Exception exc) {
            JOptionPane.showMessageDialog(null, "Error: " + exc.getMessage());
        } finally {
            try {
                if (pst != null) {
                    pst.close();
                }
                if (con != null) {
                    con.close();
                }
            } catch (Exception e) {
                // Tangani penutupan statement dan koneksi
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

