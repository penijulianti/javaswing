    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
            koneksi();

            // Mendapatkan nilai dari jComboBox
            String selectedStatus = jComboBox1.getSelectedItem().toString();

            // Query SQL untuk update data
            String SQL = "update barang set nama_barang=?, harga=?, stok=?, satuan=? where kd_barang=?";
            pst = con.prepareStatement(SQL);

            // Mengisi parameter query dengan nilai dari field-text
            pst.setString(1, jTextField2.getText());
            pst.setString(2, jTextField3.getText());
            pst.setString(3, jTextField4.getText());
            pst.setString(4, selectedStatus); // Menggunakan nilai dari jComboBox untuk Satuan
            pst.setString(5, jTextField1.getText()); // kd_barang untuk kondisi WHERE

            // Eksekusi query update
            int rowsUpdated = pst.executeUpdate();

            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(null, "Data Berhasil Diubah");
                display(); // Memperbarui tabel setelah update
                Clear(); // Membersihkan field-text setelah update
            } else {
                JOptionPane.showMessageDialog(null, "Data Gagal Diubah");
            }

        } catch (Exception exc) {
            JOptionPane.showMessageDialog(null, "Error: " + exc.getMessage());
        } finally {
            try {
                if (pst != null) {
                    pst.close();
                }
                if (con != null) {
                    con.close();
                }
            } catch (Exception e) {
                // Tangani penutupan statement dan koneksi
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

