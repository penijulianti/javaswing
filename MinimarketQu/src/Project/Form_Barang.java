    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        try {
            koneksi();
            int row = jTable1.getSelectedRow();
            String tabel_klik = (jTable1.getModel().getValueAt(row, 0).toString());
            String query = "select * from barang where kd_barang = ?";
            pst = con.prepareStatement(query);
            pst.setString(1, tabel_klik);
            ResultSet sql = pst.executeQuery();
            if (sql.next()) {
                String KD = sql.getString("kd_barang");
                jTextField1.setText(KD);
                String NamaBRG = sql.getString("nama_barang");
                jTextField2.setText(NamaBRG);
                String Harga = sql.getString("harga");
                jTextField3.setText(Harga);
                String Stok = sql.getString("stok");
                jTextField4.setText(Stok);
                String satuan = sql.getString("satuan");
                switch (satuan) {
                    case "Gram":
                        jComboBox1.setSelectedIndex(1); // Indeks 1 untuk "Gram"
                        break;
                    case "Mili":
                        jComboBox1.setSelectedIndex(2); // Indeks 2 untuk "Mili"
                        break;
                    case "Pcs":
                        jComboBox1.setSelectedIndex(3); // Indeks 3 untuk "Pcs"
                        break;
                    case "Lusin":
                        jComboBox1.setSelectedIndex(4); // Indeks 4 untuk "Lusin"
                        break;
                    case "Pack":
                        jComboBox1.setSelectedIndex(5); // Indeks 5 untuk "Pack"
                        break;
                    case "Dus":
                        jComboBox1.setSelectedIndex(6); // Indeks 6 untuk "Dus"
                        break;
                    case "Kilo Gram":
                        jComboBox1.setSelectedIndex(7); // Indeks 7 untuk "Kilo Gram"
                        break;
                    default:
                        jComboBox1.setSelectedIndex(0); // Indeks 0 untuk opsi "..Pilih.."
                        break;
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_jTable1MouseClicked

